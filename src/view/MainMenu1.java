    public MainMenu1(Gebruiker gebruiker) {
        this.gebruiker = gebruiker;
        
        initComponents();
        WinkelApplication.setSize(new Dimension(900, 500));
        WinkelApplication.center();
        
        String naam = this.gebruiker.getGebruikersnaam();
        String type = this.gebruiker.getAccounttype();
        int length = naam.length();
        
        naam = naam.substring(0, 1).toUpperCase().concat(naam.substring(1, length));
        
        lbl_gebruikersnaam.setText("Welkom " + naam + "!");
        
        
        
        
        
        
        
        if (type.equals("cassi√®re")) {
            AlgemeneTaken ataken = new AlgemeneTaken(gebruiker);
            jPanel1.add(ataken);
        }
        
        if (type.equals("filliaalmanager") || type.equals("leverancier")) {
            BeheerderTaken btaken = new BeheerderTaken(gebruiker);
            jPanel1.add(btaken);
        }
        
        if (type.equals("leverancier")) {
            LeverancierTaken ltaken = new LeverancierTaken(gebruiker);
            jPanel1.add(ltaken);
        }
        
        if(type.equals("systeembeheerder"))
        {
            AlgemeneTaken ataken = new AlgemeneTaken(gebruiker);
            jPanel1.add(ataken);
            
            BeheerderTaken btaken = new BeheerderTaken(gebruiker);
            jPanel1.add(btaken);
            
            SysteemTaken ttaken = new SysteemTaken(gebruiker);
            jPanel1.add(ttaken);
            
            LeverancierTaken ltaken = new LeverancierTaken(gebruiker);
            jPanel1.add(ltaken);
        }
        
        Timer timer = new Timer(1000, new ActionListener() {
            public void actionPerformed(ActionEvent e) {
                DateFormat dateFormat = new SimpleDateFormat("EEEE, dd MMMM yyyy - HH:mm:ss");
                Date date = new Date();
                lbl_date.setText(dateFormat.format(date));
            }
        });
        
        timer.start();
   }
    
