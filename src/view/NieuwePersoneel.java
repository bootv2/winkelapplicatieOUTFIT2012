    private void btn_toevoegenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_toevoegenActionPerformed
        Gebruiker gebruiker = new Gebruiker();
        gebruiker.setVoornaam(tf_voornaam.getText());
        gebruiker.setAchternaam(tf_achternaam.getText());
        gebruiker.setGebruikersnaam(tf_gebruikersnaam.getText());
        gebruiker.setWachtwoord(pf_wachtwoord.getText());
        gebruiker.setEmail(tf_email.getText());
        gebruiker.setAccounttype(cb_accounttype.getSelectedItem().toString());
        
        WinkelApplication.getQueryManager().addGebruiker(gebruiker);
        clearTextfields();
        overzicht.refreshTable();
        setVisible(false);
    }//GEN-LAST:event_btn_toevoegenActionPerformed

